#Concatenation of two strings
a = input("Enter number 1: ")
b = input("Enter number 2: ")

print("Number 1 is: ", a)
print("Number 2 is: ", b)
print("Sum of number 1 and number 2 is: ", a + b)

#Type conversion
a = int(input("Enter number 1: "))
b = int(input("Enter number 2: "))

print("Number 1 is: ", a)
print("Number 2 is: ", b)
print("Sum of number 1 and number 2 is: ", a + b)